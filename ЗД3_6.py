def is_same_color(col1, row1, col2, row2):
    # Вычисляем цвет первой клетки
    color1 = (col1 + row1) % 2
    # Вычисляем цвет второй клетки
    color2 = (col2 + row2) % 2
    
    # Проверяем, одинакового ли цвета клетки
    if color1 == color2:
        print("Да")
    else:
        print("Нет")

# Пример использования:
col1 = int(input("Введите номер столбца для первой клетки (1-8): "))
row1 = int(input("Введите номер строки для первой клетки (1-8): "))
col2 = int(input("Введите номер столбца для второй клетки (1-8): "))
row2 = int(input("Введите номер строки для второй клетки (1-8): "))

is_same_color(col1, row1, col2, row2)